#include "env_variables.h"
#include "../utils/utils.h"

void	set_term(char *name, char *value)
{
 	if (streq(name, "TERM"))
	{
		g_minishell_data.term.value = ft_strdup(value);
		g_minishell_data.term.name = ft_strdup("TERM");
	}
}
